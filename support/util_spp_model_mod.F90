SUBROUTINE CRC64_SPP_MODEL (YD, KLUN, CDPATH)
USE UTIL_spp_pert_MOD
USE CRC64_INTRINSIC

IMPLICIT NONE
TYPE (SPP_MODEL), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%VERSION), CDPATH//'%VERSION'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%NMAX), CDPATH//'%NMAX'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%DEFINED_PERTS), CDPATH//'%DEFINED_PERTS'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%ACTIVE_PERTS), CDPATH//'%ACTIVE_PERTS'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%NDEF), CDPATH//'%NDEF'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%NACT), CDPATH//'%NACT'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%NRFTOTAL), CDPATH//'%NRFTOTAL'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%NRFTOTAL_RADGRID), CDPATH//'%NRFTOTAL_RADGRID'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%NSEED_OFF), CDPATH//'%NSEED_OFF'

DO J1 = LBOUND (YD%PNDEF, 1), UBOUND (YD%PNDEF, 1)
  WRITE (CLIND, '("(",I0,")")') J1
  CALL CRC64_spp_pert (YD%PNDEF (J1), KLUN, CDPATH//'%PNDEF'//TRIM(CLIND))
ENDDO

DO J1 = LBOUND (YD%PN, 1), UBOUND (YD%PN, 1)
  WRITE (CLIND, '("(",I0,")")') J1
  CALL CRC64_spp_pert (YD%PN (J1), KLUN, CDPATH//'%PN'//TRIM(CLIND))
ENDDO

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%TAU), CDPATH//'%TAU'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%XLCOR), CDPATH//'%XLCOR'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%SDEV), CDPATH//'%SDEV'

WRITE (KLUN, '(Z16.16," ",A)') CRC64 (YD%KSEED_OFF), CDPATH//'%KSEED_OFF'

END SUBROUTINE

