SUBROUTINE LOAD_SPP_PERT (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (SPP_PERT), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) YD%LABEL
READ (KLUN) YD%ON
READ (KLUN) YD%IDISTR
ALLOCATE (ZTMP1(LBOUND(YD%XMAG,1):UBOUND(YD%XMAG,1)))
READ (KLUN) ZTMP1
YD%XMAG = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%MU,1):UBOUND(YD%MU,1)))
READ (KLUN) ZTMP1
YD%MU = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%XCLIPMIN = ZTMP0
READ (KLUN) ZTMP0
YD%XCLIPMAX = ZTMP0
READ (KLUN) ZTMP0
YD%XUNIFORM_OFFSET = ZTMP0
READ (KLUN) YD%NMAG
READ (KLUN) YD%LN1
READ (KLUN) YD%NSEED_OFF
READ (KLUN) YD%NOFF
READ (KLUN) YD%NRF
READ (KLUN) YD%NRF_RADGRID
READ (KLUN) YD%RADGRID
READ (KLUN) YD%RF_PERT_LABEL
READ (KLUN) YD%IC
READ (KLUN) YD%MP
READ (KLUN) YD%MP_RADGRID
READ (KLUN) ZTMP0
YD%TAU = ZTMP0
READ (KLUN) ZTMP0
YD%SDEV = ZTMP0
READ (KLUN) ZTMP0
YD%XLCOR = ZTMP0
END SUBROUTINE


