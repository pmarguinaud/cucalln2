SUBROUTINE LOAD_TEGWWMS (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEGWWMS), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) YD%LOZPR
READ (KLUN) YD%LGACALC
READ (KLUN) YD%LGSATL
READ (KLUN) YD%LGINDL
READ (KLUN) YD%NLAUNCHL
READ (KLUN) YD%NLAUNCHLEV
READ (KLUN) YD%NLAUNCH
READ (KLUN) YD%NSLOPE
READ (KLUN) YD%NGAUSS
ALLOCATE (ZTMP1(LBOUND(YD%GFLUXLAUNL,1):UBOUND(YD%GFLUXLAUNL,1)))
READ (KLUN) ZTMP1
YD%GFLUXLAUNL = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%GFLUXLAUN = ZTMP0
ALLOCATE (ZTMP1(LBOUND(YD%GMSTAR_L,1):UBOUND(YD%GMSTAR_L,1)))
READ (KLUN) ZTMP1
YD%GMSTAR_L = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%GCSTAR = ZTMP0
READ (KLUN) ZTMP0
YD%GPTWO = ZTMP0
READ (KLUN) ZTMP0
YD%GTPHYGWWMS = ZTMP0
READ (KLUN) ZTMP0
YD%GGAUSSA = ZTMP0
ALLOCATE (ZTMP1(LBOUND(YD%GGAUSSB,1):UBOUND(YD%GGAUSSB,1)))
READ (KLUN) ZTMP1
YD%GGAUSSB = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%GCOEFF = ZTMP0
END SUBROUTINE


