
SUBROUTINE SATUR_OPENACC (YDTHF, YDCST, KIDIA, KFDIA, KLON, KTDIA&
&, KLEV, LDPHYLIN, PAPRSF, PT, PQSAT, KFLAG, YDSTACK)
!$acc routine (SATUR_OPENACC) seq
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
USE YOETHF,ONLY:TTHF
USE ABOR1_ACC_MOD
USE STACK_MOD
#include "stack.h"

IMPLICIT NONE

TYPE (TTHF), INTENT (IN)::YDTHF
TYPE (TCST), INTENT (IN)::YDCST
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KTDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
LOGICAL, INTENT (IN)::LDPHYLIN
REAL (KIND=JPRB), INTENT (IN)::PAPRSF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PT (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PQSAT (KLON, KLEV)
INTEGER (KIND=JPIM), INTENT (IN)::KFLAG
TYPE(STACK) :: YDSTACK
TYPE(STACK) :: YLSTACK

INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JLEV

REAL (KIND=JPRB)::ZTARG
REAL (KIND=JPRB)::ZQS
REAL (KIND=JPRB)::ZQMAX
REAL (KIND=JPRB)::ZFOEEW
REAL (KIND=JPRB)::ZEW
REAL (KIND=JPRB)::ZCOR

REAL (KIND=JPRB)::ZFOEEWI
REAL (KIND=JPRB)::ZFOEEWL
REAL (KIND=JPRB)::ZALFA

#include "fcttre.func.h"

YLSTACK = YDSTACK




JLON = KIDIA


ZQMAX=0.5_JPRB

IF (LDPHYLIN) THEN

  DO JLEV=KTDIA, KLEV
    
    ZTARG=PT (JLON, JLEV)
    ZALFA=FOEALFA (ZTARG)
    ZFOEEWL=YDTHF%R2ES*EXP (YDTHF%R3LES*(ZTARG-YDCST%RTT)/(ZTARG-YDTHF%R4LES))
    ZFOEEWI=YDTHF%R2ES*EXP (YDTHF%R3IES*(ZTARG-YDCST%RTT)/(ZTARG-YDTHF%R4IES))
    ZFOEEW=ZALFA*ZFOEEWL+(1.0_JPRB-ZALFA)*ZFOEEWI
    ZQS=ZFOEEW/PAPRSF (JLON, JLEV)

    IF (ZQS>ZQMAX) THEN
      ZQS=ZQMAX
    ENDIF

    ZCOR=1.0_JPRB/(1.0_JPRB-YDCST%RETV*ZQS)
    PQSAT (JLON, JLEV)=ZQS*ZCOR
  
  ENDDO

ELSE

  DO JLEV=KTDIA, KLEV

    

    IF (KFLAG==1) THEN
      ZEW=FOEEWMCU (PT (JLON, JLEV))
    ELSE
      ZEW=FOEEWM (PT (JLON, JLEV))
    ENDIF

    ZQS=ZEW/PAPRSF (JLON, JLEV)
    ZQS=MIN (ZQMAX, ZQS)
    ZCOR=1.0_JPRB/(1.0_JPRB-YDCST%RETV*ZQS)
    PQSAT (JLON, JLEV)=ZQS*ZCOR
  
  ENDDO

ENDIF



ENDSUBROUTINE SATUR_OPENACC

