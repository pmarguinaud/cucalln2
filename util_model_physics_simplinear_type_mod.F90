CALL CRC64_TEGWDWMS (YD%YREGWDWMS, KLUN, CDPATH//'%YREGWDWMS'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TECUMF2 (YD%YRECUMF2, KLUN, CDPATH//'%YRECUMF2'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TPHLC (YD%YRPHLC, KLUN, CDPATH//'%YRPHLC'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TPHNC (YD%YRPHNC, KLUN, CDPATH//'%YRPHNC'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TNCL (YD%YRNCL, KLUN, CDPATH//'%YRNCL'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TSRFTLAD (YD%YRSRFTLAD, KLUN, CDPATH//'%YRSRFTLAD'//TRIM(CLIND))

