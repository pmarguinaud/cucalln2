SUBROUTINE SAVE_MODEL_PHYSICS_SIMPLINEAR_TYPE (KLUN, YD)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LGPHIST
CALL SAVE_TEPHLI (KLUN, YD%YREPHLI)
CALL SAVE_TCUMFS (KLUN, YD%YRCUMFS)
CALL SAVE_TEGWDWMS (KLUN, YD%YREGWDWMS)
CALL SAVE_TECUMF2 (KLUN, YD%YRECUMF2)
CALL SAVE_TPHLC (KLUN, YD%YRPHLC)
CALL SAVE_TPHNC (KLUN, YD%YRPHNC)
CALL SAVE_TNCL (KLUN, YD%YRNCL)
CALL SAVE_TSRFTLAD (KLUN, YD%YRSRFTLAD)
LGPHIST = ASSOCIATED (YD%GPHIST)
WRITE (KLUN) LGPHIST
IF (LGPHIST) THEN
  WRITE (KLUN) LBOUND (YD%GPHIST)
  WRITE (KLUN) UBOUND (YD%GPHIST)
  DO J1 = LBOUND (YD%GPHIST, 1), UBOUND (YD%GPHIST, 1)
    CALL SAVE_SPHYS_HIST_TYPE (KLUN, YD%GPHIST (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_MODEL_PHYSICS_SIMPLINEAR_TYPE (KLUN, YD)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGPHIST
CALL LOAD_TEPHLI (KLUN, YD%YREPHLI)
CALL LOAD_TCUMFS (KLUN, YD%YRCUMFS)
CALL LOAD_TEGWDWMS (KLUN, YD%YREGWDWMS)
CALL LOAD_TECUMF2 (KLUN, YD%YRECUMF2)
CALL LOAD_TPHLC (KLUN, YD%YRPHLC)
CALL LOAD_TPHNC (KLUN, YD%YRPHNC)
CALL LOAD_TNCL (KLUN, YD%YRNCL)
CALL LOAD_TSRFTLAD (KLUN, YD%YRSRFTLAD)
READ (KLUN) LGPHIST
IF (LGPHIST) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%GPHIST (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%GPHIST, 1), UBOUND (YD%GPHIST, 1)
    CALL LOAD_SPHYS_HIST_TYPE (KLUN, YD%GPHIST (J1))
  ENDDO
ELSE
  NULLIFY (YD%GPHIST)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_MODEL_PHYSICS_SIMPLINEAR_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LGPHIST

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$acc enter data create (YD)
  !$acc update device (YD)
ENDIF
CALL COPY_TEPHLI (YD%YREPHLI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TCUMFS (YD%YRCUMFS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TEGWDWMS (YD%YREGWDWMS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TECUMF2 (YD%YRECUMF2, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TPHLC (YD%YRPHLC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TPHNC (YD%YRPHNC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TNCL (YD%YRNCL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSRFTLAD (YD%YRSRFTLAD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGPHIST = ASSOCIATED (YD%GPHIST)
IF (LGPHIST) THEN
  !$acc enter data create (YD%GPHIST)
  !$acc update device (YD%GPHIST)
  DO J1 = LBOUND (YD%GPHIST, 1), UBOUND (YD%GPHIST, 1)
    CALL COPY_SPHYS_HIST_TYPE (YD%GPHIST (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$acc enter data attach (YD%GPHIST)
ENDIF

END SUBROUTINE

